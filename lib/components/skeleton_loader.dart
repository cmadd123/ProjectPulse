/// Convenience widget: shows N skeleton cards in a column.
class SkeletonProjectList extends StatelessWidget {
  final int count;
  const SkeletonProjectList({super.key, this.count = 3});

  @override
  Widget build(BuildContext context) {
    return Padding(
      padding: const EdgeInsets.all(16),
      child: Column(
        children: List.generate(count, (_) => const SkeletonCard()),
      ),
    );
  }
}
